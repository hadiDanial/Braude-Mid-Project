package server;

public class ConsoleString {
    private String str;

    public ConsoleString(String str) {
        this.str = str;
    }

    
    /** 
     * @return String
     */
    public String getStr() {
        return str;
    }

    
    /** 
     * @param str
     */
    public void setStr(String str) {
        this.str = str;
    }

    
    /** 
     * @return String
     */
    @Override
    public String toString() {
        return this.toString();
    }
}
